const add = function(val1, val2) {
	let sum = val1 + val2;
  return sum;
};

const subtract = function(val1, val2) {
  let diff = val1 - val2;
  return diff;
};

const multiply = function(val1, val2) {
    let prod = val1 * val2;
    return prod;
  };

const divide = function(val1, val2) {
    let dividend = val1 / val2;
    return dividend;
  };